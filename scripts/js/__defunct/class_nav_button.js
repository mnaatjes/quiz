class NavButton extends Button {
    constructor(btn_id, page){
        super(btn_id);
        this.page       = page;
        this.tooltip    = new Tooltip(this.node);

    }
    /*------------------------------------------------------*/
    /**
     * @name onMouseOver
     * @type {Function}
     */
    /*------------------------------------------------------*/
    onMouseOver(){
        this.tooltip.showHint();
    }
    /*------------------------------------------------------*/
    /**
     * @name onClick
     * @type {Function}
     */
    /*------------------------------------------------------*/
    onClick(){
    }
    /*------------------------------------------------------*/
    /**
     * @name onMouseOut
     * @type {Function}
     */
    /*------------------------------------------------------*/
    onMouseOut(){
        this.tooltip.hideHint();
    }
}